/// This class provide floating button position set
/// EX: Stack Position
class Position {
  ///Floating button top position set
  final double? top;

  ///Floating button bottom position set
  final double? bottom;

  ///Floating left top position set
  final double? left;

  ///Floating button right position set
  final double? right;

  Position({this.top, this.bottom, this.left, this.right});
}
